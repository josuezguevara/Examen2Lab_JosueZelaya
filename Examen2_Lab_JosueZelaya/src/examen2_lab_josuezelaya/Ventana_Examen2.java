    private void boton_guardar_cancionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_boton_guardar_cancionMouseClicked
        if (album.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se ha creado un album");
        } else {
            System.out.println("entro");
            try {
                String nombre = tf_nombre_cancion.getText();
                String artista = tf_artista_cancion.getText();
                int duracion = Integer.parseInt(tf_duracion_cancion.getText());
                String genero = tf_genero_cancion.getText();
                canciones.add(new Canciones(nombre, artista, duracion, genero));
                JOptionPane.showMessageDialog(this, "Se agrego exitosamente");
                
                String nombre_album=cb_album_cancion.getSelectedItem().toString();
                for (Album temp : album) {
                    if (nombre_album.equals(temp.getNombre())){
                        temp.setCanciones_agregadas(canciones);
                    }
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_boton_guardar_cancionMouseClicked

