    public void Guardar(ArrayList usuarios) {
        try {
            FileOutputStream archivo = new FileOutputStream("./usuario.cbm");
            ObjectOutputStream os = new ObjectOutputStream(archivo);
            os.writeObject(usuarios);
            os.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    public void Abrir() {
        try {
            FileInputStream entrada = new FileInputStream("./usuario.cbm");
            ObjectInputStream oi = new ObjectInputStream(entrada);
            try {
                usuarios = (ArrayList<Usuario>) oi.readObject();
            } catch (ClassNotFoundException e) {

            }
        } catch (IOException e) {
        }
    }

